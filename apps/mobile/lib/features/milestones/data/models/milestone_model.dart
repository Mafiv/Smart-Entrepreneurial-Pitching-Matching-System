import '../../domain/entities/milestone_entity.dart';

class MilestoneModel extends MilestoneEntity {
  const MilestoneModel({
    required String id,
    required String submissionId,
    required String matchResultId,
    required String entrepreneurId,
    required String investorId,
    required String createdBy,
    required String title,
    required String? description,
    required double amount,
    required String currency,
    required DateTime dueDate,
    required List<MilestoneEvidenceDocument> evidenceDocuments,
    required DateTime? submittedAt,
    required DateTime? verifiedAt,
    required String? verifiedBy,
    required String? verificationNotes,
    required MilestoneEscrowStatus escrowStatus,
    required String? escrowReference,
    required DateTime? paymentReleasedAt,
    required String? paymentReference,
    required MilestoneStatus status,
    required String? projectId,
    required String? proof,
    required String? feedback,
    required DateTime createdAt,
    required DateTime updatedAt,
  }) : super(
          id: id,
          submissionId: submissionId,
          matchResultId: matchResultId,
          entrepreneurId: entrepreneurId,
          investorId: investorId,
          createdBy: createdBy,
          title: title,
          description: description,
          amount: amount,
          currency: currency,
          dueDate: dueDate,
          evidenceDocuments: evidenceDocuments,
          submittedAt: submittedAt,
          verifiedAt: verifiedAt,
          verifiedBy: verifiedBy,
          verificationNotes: verificationNotes,
          escrowStatus: escrowStatus,
          escrowReference: escrowReference,
          paymentReleasedAt: paymentReleasedAt,
          paymentReference: paymentReference,
          status: status,
          projectId: projectId,
          proof: proof,
          feedback: feedback,
          createdAt: createdAt,
          updatedAt: updatedAt,
        );

  factory MilestoneModel.fromJson(Map<String, dynamic> json) {
    final entity = MilestoneEntity.fromJson(json);
    return MilestoneModel(
      id: entity.id,
      submissionId: entity.submissionId,
      matchResultId: entity.matchResultId,
      entrepreneurId: entity.entrepreneurId,
      investorId: entity.investorId,
      createdBy: entity.createdBy,
      title: entity.title,
      description: entity.description,
      amount: entity.amount,
      currency: entity.currency,
      dueDate: entity.dueDate,
      evidenceDocuments: entity.evidenceDocuments,
      submittedAt: entity.submittedAt,
      verifiedAt: entity.verifiedAt,
      verifiedBy: entity.verifiedBy,
      verificationNotes: entity.verificationNotes,
      escrowStatus: entity.escrowStatus,
      escrowReference: entity.escrowReference,
      paymentReleasedAt: entity.paymentReleasedAt,
      paymentReference: entity.paymentReference,
      status: entity.status,
      projectId: entity.projectId,
      proof: entity.proof,
      feedback: entity.feedback,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
    );
  }

  factory MilestoneModel.fromEntity(MilestoneEntity entity) {
    return MilestoneModel(
      id: entity.id,
      submissionId: entity.submissionId,
      matchResultId: entity.matchResultId,
      entrepreneurId: entity.entrepreneurId,
      investorId: entity.investorId,
      createdBy: entity.createdBy,
      title: entity.title,
      description: entity.description,
      amount: entity.amount,
      currency: entity.currency,
      dueDate: entity.dueDate,
      evidenceDocuments: entity.evidenceDocuments,
      submittedAt: entity.submittedAt,
      verifiedAt: entity.verifiedAt,
      verifiedBy: entity.verifiedBy,
      verificationNotes: entity.verificationNotes,
      escrowStatus: entity.escrowStatus,
      escrowReference: entity.escrowReference,
      paymentReleasedAt: entity.paymentReleasedAt,
      paymentReference: entity.paymentReference,
      status: entity.status,
      projectId: entity.projectId,
      proof: entity.proof,
      feedback: entity.feedback,
      createdAt: entity.createdAt,
      updatedAt: entity.updatedAt,
    );
  }
}
